u8 USART3_RX_BUF[USART3_MAX_RECV_LEN]; 				    //接收缓冲,最大USART2_MAX_RECV_LEN个字节.
//通过判断接收连续2个字符之间的时间差不大于100ms来决定是不是一次连续的数据.
//如果2个字符接收间隔超过100ms,则认为不是1次连续数据.也就是超过100ms没有接收到
//任何数据,则表示此次接收完毕.
//接收到的数据状态
//[15]:0,没有接收到数据;1,接收到了一批数据.
//[14:0]:接收到的数据长度
u16 USART3_RX_STA=0;   	 
void USART3_IRQHandler(void)
{
	u8 Res;	    
	if((__HAL_UART_GET_FLAG(&huart3,UART_FLAG_RXNE)!=RESET))//接收到数据
	{	 
		HAL_UART_Receive(&huart3,&Res,1,1000); 
		if((USART3_RX_STA&0x8000)==0)//接收完的一批数据,还没有被处理,则不再接收其他数据
		{ 		
			if(USART3_RX_STA<USART3_MAX_RECV_LEN)		//还可以接收数据
			{
				__HAL_TIM_SET_COUNTER(&htim3,0);//计数器清空        				 
				if(USART3_RX_STA==0)		
					__HAL_TIM_ENABLE(&htim3);  //使能定时器3 
				USART3_RX_BUF[USART3_RX_STA++]=Res;		//记录接收到的值	 
			}else 
			{
				USART3_RX_STA|=1<<15;					//强制标记接收完成
			} 
		}  	
  }										 
}  

